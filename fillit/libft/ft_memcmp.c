#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*a;
	unsigned char	*b;
	size_t			i;

	a = (unsigned char *)s1;
	b = (unsigned char *)s2;
	i = 0;
	while (n > 0 && a[i] == b[i])
	{
		i++;
		n--;
	}
	if (n == 0)
		return (0);
	else
		return (a[i] - b[i]);
}
