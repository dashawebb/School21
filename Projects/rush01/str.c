#include <unistd.h>

void	put_sudoku(char **x)
{
	int i;
	int j;

	i = 0;
	while (i < 9)
	{
		j = 0;
		while (j < 9)
		{
			write(1, &(x[i][j]), 1);
			if (j == 8)
				write(1, "\n", 1);
			else
				write(1, " ", 1);
			j++;
		}
		i++;
	}
}

void	putstr(char *x)
{
	int q;

	q = 0;
	while (x[q] != '\0')
		write(1, &x[q++], 1);
}
