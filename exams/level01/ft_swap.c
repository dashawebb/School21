#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int temp;
	temp = *a;
	*a = *b;
	*b = temp;
}

int main()
{
	int q;
	int w;
	int *z;
	int *y;

	q = 10;
	w = 5;
	z = &q;
	y = &w;
	ft_swap(z, y);
	printf("%d", w);
}
