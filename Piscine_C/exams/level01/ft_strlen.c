#include <stdio.h>

int	ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

int main()
{
	int s;
	char *s1;
	s1 = "Hello";
	s = ft_strlen(s1);
	printf("%d", s);
}
