int	ft_sqrt(int nb)
{
	int k;

	k = 0;
	if (nb < 0)
	{
		return (0);
	}
	while (k * k <= nb)
	{
		k++;
	}
	k--;
	if (k * k == nb)
	{
		return (k);
	}
	else
	{
		return (0);
	}
}
